func (c *spoc) getModel(a *ast.Attribute, ctx string) *model {
	l := c.getValueList(a, ctx)
	m := l[0]
	attributes := l[1:]
	orig, found := routerInfo[m]
	if !found {
		c.err("Unknown model in %s: %s", ctx, m)

		// Prevent further errors.
		return &model{name: m}
	}
	info := *orig
	if len(attributes) > 0 {
		add := ""
		for _, att := range attributes {
			add += ", " + att
			switch m {
			case "IOS":
				switch att {
				case "FW":
					info.stateless = false
				default:
					goto FAIL
				}
			case "ASA":
				switch att {
				case "VPN":
					info.crypto = "ASA_VPN"
					info.doAuth = true
				case "CONTEXT":
					info.cryptoInContext = true
				default:
					goto FAIL
				}
			case "NSX":
				switch att {
				case "T0":
					info.tier = "0"
				case "T1":
					info.tier = "1"
				default:
					goto FAIL
				}
			default:
				goto FAIL
			}
			continue
		FAIL:
			c.err("Unknown extension in '%s' of %s: %s", a.Name, ctx, att)
		}
		info.name += add
	}
	return &info
}

